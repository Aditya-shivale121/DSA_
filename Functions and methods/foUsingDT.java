
public class foUsingDT{
    public static int sum (int a , int b){
        int sum = a + b;
        return sum ;
        
    }
    public static  float  sum (float a , float b){
        float sum = a + b;
        return sum ;
        
    }
    public static void main(String[] args) {
        System.out.println(sum(5f, 3f));
        System.out.println(sum(1, 2));
        
    }
}